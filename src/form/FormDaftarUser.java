    public void showTable(){
        
        DefaultTableModel model= new DefaultTableModel(); 
        model.addColumn("ID User"); 
        model.addColumn("Nama User");
        model.addColumn("Jabatan");
        model.addColumn("Password");
        dataTable.setModel(model);
        
        String sql = "SELECT * FROM tb_daftaruser ";
        
        con = new DBUtils().getKoneksi();
        int cnt = 1;
        try {
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            
            while (rs.next()){
                model.addRow(new Object[]{
                    rs.getString("iduser"), 
                    rs.getString("namauser"), 
                    rs.getString("level"),
                    rs.getString("password"),
                    } 
                );
            }    
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
     
//        jumlahLabel.setText(String.valueOf(dataTable.getRowCount()));
        model.fireTableDataChanged();
        
//        dataTable.getColumnModel().getColumn(0).setMaxWidth(50);
        
        dataTable.addMouseListener(new MouseAdapter() {
            
            @Override
            public void mousePressed(MouseEvent event) {
                Point point = event.getPoint();
                int currentRow = dataTable.rowAtPoint(point);
                System.out.println("currentRow == "+currentRow);
                
                String userId = (String)dataTable.getValueAt(currentRow, 0);
                String nama = (String)dataTable.getValueAt(currentRow, 1);
                String jabatan = (String)dataTable.getValueAt(currentRow, 2);
                String password = (String)dataTable.getValueAt(currentRow, 3);
                
                System.out.println(userId);
                System.out.println(nama);
                
                userIdField.setEnabled(false);
                userIdField.setText(userId);
                userNameField.setText(nama);
                jabatanCB.setSelectedItem(jabatan);
                //passwordField.setText(password);
                passwordField.setText(EncryptionUtils.decrypt((password), "p@ssw0rd"));
                //String wew = void EncryptionUtils.encrypt(passwordField.setText(password), "p@ssw0rd");
                //ps.setString(4, EncryptionUtils.encrypt(passwordField.getText(), "p@ssw0rd"));
                
                dataTable.setRowSelectionInterval(currentRow, currentRow);
            }
        });
    }

