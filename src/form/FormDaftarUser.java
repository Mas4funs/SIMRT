    public void hapusRecord(String kode){
        String sql = "DELETE FROM tb_daftaruser WHERE iduser = ? ";
        con = new DBUtils().getKoneksi();
        try {
            ps = con.prepareStatement(sql);
            ps.setString(1, kode);
            ps.execute();
            
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Data gagal dihapus\n"+ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        showTable();
    }
    
    public void tambahRecord(){
        String sql = "INSERT INTO tb_daftaruser(iduser, namauser, level, password) VALUES (?, ?, ?, ?) ";
        try {
            ps = con.prepareStatement(sql);
            ps.setString(1, userIdField.getText());
            ps.setString(2, userNameField.getText());
            ps.setString(3, jabatanCB.getSelectedItem().toString());
            //ps.setString(4, passwordField.getText());
            ps.setString(4, EncryptionUtils.encrypt(passwordField.getText(), "p@ssw0rd"));
            ps.execute();
            
            JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Data gagal ditambahkan", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void updateRecord(){
        String sql = "UPDATE tb_daftaruser SET namauser = ?, level = ?, password = ? WHERE iduser = ? ";
        con = new DBUtils().getKoneksi();
        try {
            ps = con.prepareStatement(sql);
            ps.setString(1, userNameField.getText());
            ps.setString(2, jabatanCB.getSelectedItem().toString());
            //ps.setString(3, passwordField.getText());
            ps.setString(3, EncryptionUtils.encrypt(passwordField.getText(), "p@ssw0rd"));
            ps.setString(4, userIdField.getText());
            ps.execute();
            
            JOptionPane.showMessageDialog(null, "Data berhasil diubah", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Data gagal diubah", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }
    
